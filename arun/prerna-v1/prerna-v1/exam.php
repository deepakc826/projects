            <h2>IELTS Exam Pattern:</h2>
            <p>Listening: The listening section consists of four recorded audio passages played through headphones. Candidates answer questions related to the content of these recordings. The duration of the listening test is approximately 30 minutes, including the time to transfer answers to the answer sheet.</p>

            <p>Reading: The reading section contains three or sometimes four texts taken from books, journals, magazines, or newspapers. Candidates answer questions based on the information provided in the texts. The duration of the reading test is 60 minutes.</p>

            <p>Writing: The writing section comprises two tasks. In Task 1, candidates have to describe information presented in a graph, table, chart, or diagram. In Task 2, candidates must write an essay responding to a specific topic or question. The duration of the writing test is 60 minutes.</p>

            <p>Speaking: The speaking test is a face-to-face interview conducted by a certified IELTS examiner. It is designed to assess the candidate's spoken English skills. The speaking test is usually held on a different day from the other sections and can last around 11 to 14 minutes.</p>
